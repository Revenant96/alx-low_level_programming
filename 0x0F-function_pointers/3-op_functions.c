#include "3-calc.h"

/**
 * op_add - function to add 2 ints
 * @a: first int
 * @b: second int
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract
 * @a: first int
 * @b: second int
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiplay 2 ints
 * @a: first int
 * @b: second int
 * Return: multiplcation result
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function to divide 2 ints
 * @a: first int
 * @b: second int
 * Return: divison result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to mod 2 ints
 * @a: first int
 * @b: second int
 * Return: modulos
 */

int op_mod(int a, int b)
{
	return (a % b);
}

